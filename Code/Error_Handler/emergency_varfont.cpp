static inline pixel* step_down(pixel* pixel_addr)
{
	return pixel_addr + (gimp_image.bytes_per_pixel * gimp_image.width);
}

// +-----------------------------------+-------------------------+-----------------------
// | TITLE:                            | AUTHOR(s): Rob          | DATE STARTED: 17 Nov 2005
// +
// | 
// +----------------------------------------------------------------ROUTINE HEADER----

static inline pixel* step_across(pixel* pixel_addr)
{
	return pixel_addr + gimp_image.bytes_per_pixel;
}


// +-----------------------------------+-------------------------+-----------------------
// | TITLE:                            | AUTHOR(s): Rob          | DATE STARTED: 17 Nov 2005
// +
// | 
// +----------------------------------------------------------------ROUTINE HEADER----

static unsigned int get_character_start(char my_char)
{
	if(my_char < 32) my_char = '?';
#if CHAR_MAX > 127
	if(my_char > 127) my_char = '?';
#endif
	my_char -= 32;
	
	// scan along the character rows trying 
	pixel* px_ptr = gimp_image.pixel_data;	
	if(*px_ptr != 0)							// no top pixel, then we don't understand the format
	{
		fprintf(stderr,"Emergency Font doesn't have correct pixel arrangement");
		exit(1);
	}
	
	unsigned int last_start_pixel = 0;
	for(unsigned int x=0; x < gimp_image.width; x++)
	{
		if(my_char == 0) break;			// ensure we get the case of character = space

		px_ptr = step_across(px_ptr);	// look for the next marker pixel

		if(*px_ptr == 0)
		{
			last_start_pixel = x+1;		// record position of next marker pixel
			my_char--;					// next marker means another character
		}		
	}
	return last_start_pixel;
}

// +-----------------------------------+-------------------------+-----------------------
// | TITLE:                            | AUTHOR(s): Rob          | DATE STARTED: 17 Nov 2005
// +
// | 
// +----------------------------------------------------------------ROUTINE HEADER----

static int get_this_character_width(unsigned int character_start_index)
{
	// scan along the character rows trying 
	pixel* px_ptr = gimp_image.pixel_data + (character_start_index*gimp_image.bytes_per_pixel);	
	if(*px_ptr != 0)							// no top pixel, then we don't understand the format
	{
		fprintf(stderr,"Emergency Font doesn't have correct pixel arrangement");
		exit(1);
	}
	
	unsigned int x;		// need it to calculate the return value
	for(x = character_start_index; x < gimp_image.width; x++)
	{
		px_ptr = step_across(px_ptr);
		if(*px_ptr == 0)
		{
			break;
		}
	}
	return (x - character_start_index)+1;
}

// +-----------------------------------+-------------------------+-----------------------
// | TITLE:                            | AUTHOR(s): Rob          | DATE STARTED: 17 Nov 2005
// +
// | 
// +----------------------------------------------------------------ROUTINE HEADER----

static int get_character_width(char c)
{
	unsigned int index = get_character_start(c);
	return get_this_character_width(index)+INTERCHARACTER_SPACE;
}

