// +-----------------------------------+-------------------------+-----------------------
// | TITLE:                            | AUTHOR(s): Rob          | DATE STARTED: 17 Nov 2005
// +
// | prints character to screen, return character width
// +----------------------------------------------------------------ROUTINE HEADER----

static int print_character(int x, int y, char character, Uint32 back_colour, Uint32 fore_colour)
{
	int ch_height = evf_get_character_height();
	
	// calculate the screen destination position
	extern Ptr screenaddr;
	extern int screenwidth;
	
	unsigned char* double_buffer=(unsigned char*)screenaddr;
	int screen_width=screenwidth;
	
	unsigned char *dest_start_addr = (unsigned char *) (double_buffer+x+(y*screen_width));
	//unsigned char *dest_addr;
	
	//  the character stuff
	unsigned int index = get_character_start(character);
	int ch_width = get_this_character_width(index);
	
	pixel* line_ch_ptr = gimp_image.pixel_data + (index*gimp_image.bytes_per_pixel);
	for(int y=0; y < ch_height; y++)
	{
		unsigned char *dest_addr = dest_start_addr;		// get screen address
		line_ch_ptr = step_down(line_ch_ptr);
		pixel* ch_ptr = line_ch_ptr;
		for(int x=0; x < ch_width; x++)
		{
			if(*ch_ptr)
			{
				*dest_addr = back_colour;
			}
			else
			{
				*dest_addr = fore_colour;
			}
			ch_ptr = step_across(ch_ptr);
			dest_addr++;
		}
		dest_start_addr += screen_width;		// next line
	}
	
	return ch_width+INTERCHARACTER_SPACE;
}

// +-----------------------------------+-------------------------+-----------------------
// | TITLE:                            | AUTHOR(s): Rob          | DATE STARTED: 17 Nov 2005
// +
// | 
// +----------------------------------------------------------------ROUTINE HEADER----

void evf_print_string(int x, int y, const char* text, Uint32 back_colour, Uint32 fore_colour)
{
	int width;
	char c;
	
	while((c = *(text++)))		// double brackets to keep gcc -Wall happy
	{
		width = print_character(x, y, c, back_colour, fore_colour);
		x += width;
	}
}


